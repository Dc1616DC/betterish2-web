export function getRelevantPack(context = {}) {
  const { isWeekend, partnerMood, kidsSick, recentArgument } = context;
  
  if (kidsSick) return expandedTemplatePacks.find(p => p.id === 'pack_004');
  if (recentArgument) return expandedTemplatePacks.find(p => p.id === 'pack_008');
  if (partnerMood === 'overwhelmed') return expandedTemplatePacks.find(p => p.id === 'pack_003');
  if (isWeekend) return expandedTemplatePacks.find(p => p.id === 'pack_005');
  
  return null;
}