// Helper functions for smart task selection
export function getTasksBySimplicity(simplicity) {
  const allTasks = Object.values(expandedTaskLibrary).flat();
  return allTasks.filter(task => task.simplicity === simplicity);
}

export function getQuickWins() {
  return getTasksBySimplicity('low').slice(0, 5);
}

export function getWeekendProjects() {
  return getTasksBySimplicity('high').filter(task => 
    ['household', 'self'].includes(task.category)
  );
}

// Smart task generation with variety
import { coreAutoTasks } from './coreAutoTasks'; // Add this at the top of your file if it's not already there

export function generateDailyTasks() {
  const relationship = coreAutoTasks.filter(t => t.category === 'relationship');
  const baby = coreAutoTasks.filter(t => t.category === 'baby');
  const household = coreAutoTasks.filter(t => t.category === 'household');

  const pick = (arr) => arr[Math.floor(Math.random() * arr.length)];

  return [pick(relationship), pick(baby), pick(household)];
}

